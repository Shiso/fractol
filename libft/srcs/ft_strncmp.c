#include "libft.h"

int			ft_strncmp(const char *s1, const char *s2, t_size n)
{
	t_size		i;

	i = 0;
	while (*s1 && *s2 && *s1 == *s2 && n > 1)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
